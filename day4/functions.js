// usual way you'll see functions written
function addSix(n) {
    return n + 6;
}
console.log(addSix(6));

// arrow functions, which is a newer more modern way
const addSeven = (n) => {
    return n + 7;
}

console.log(addSeven(7));


// either way works, so depends which way you like to write them
